	Subroutine BZ_Coords_EL1(Qc,nR,nC,xLx,xUx,xLy,iGeo,iOut)
!	xLx=16/xUx=32	
	Implicit Real(kind=8) (a-h,o-z)
	real(kind = 8)	Qc
	Dimension		Qc(nR,nC)
	real(kind = 8)	xLx,xUx,xLy,xLy4,x2Ly4,x3Ly4,aa
	integer			nR,nC
!
	Qc			= 0.			! zero all
!
!------------------------------ 
	if(iGeo == 0) Then			!---------------------- C0
		Qc(1,1)		= 0.0
		Qc(1,2)		= xLx/4.
		Qc(1,3)		= 2.*xLx/4.
		Qc(1,4)		= 3.*xLx/4.
		Qc(1,5)		= xLx
!	-----------------------------
		aa			= xLx + (xUx - xLx)/4.
!	-----------------------------
		Qc(1,6)		= 0.0
		Qc(1,7)		= 1.*aa/4.
		Qc(1,8)		= 2.*aa/4.
		Qc(1,9)		= 3.*aa/4.
		Qc(1,10)	= aa
!	-----------------------------
		aa			= xLx + 2.*(xUx - xLx)/4.
!	-----------------------------
		Qc(1,11)	= 0.0
		Qc(1,12)	= 1.*aa/4.
		Qc(1,13)	= 2.*aa/4.
		Qc(1,14)	= 3.*aa/4.
		Qc(1,15)	= aa
!	-----------------------------
		aa			= xLx + 3.*(xUx - xLx)/4.
!	-----------------------------
		Qc(1,16)	= 0.0
		Qc(1,17)	= 1.*aa/4.
		Qc(1,18)	= 2.*aa/4.
		Qc(1,19)	= 3.*aa/4.
		Qc(1,20)	= 4.*aa/4.
!	-----------------------------
		aa			= xLx + 4.*(xUx - xLx)/4.
!	-----------------------------
		Qc(1,21)	= 0.0
		Qc(1,22)	= 1.*aa/4.
		Qc(1,23)	= 2.*aa/4.
		Qc(1,24)	= 3.*aa/4.
		Qc(1,25)	= 4.*aa/4.
!------------------------------ 
	else if(iGeo == 2) Then		!---------------------- C2 todo
		Qc(1,2)		= 4.0
		Qc(1,3)		= 8.0
		Qc(1,4)		= xLx
!	-----------------------------
		aa			= xLx + (xUx - xLx)/3.
!	-----------------------------
		Qc(1,6)		= 6.0
		Qc(1,7)		= aa - 8.0
		Qc(1,8)		= aa
!	-----------------------------
		aa			= xLx + 2.*(xUx - xLx)/3.
!	-----------------------------
		Qc(1,10)	= 8.0
		Qc(1,11)	= aa - 8.0
		Qc(1,12)	= aa
		Qc(1,14)	= 12.0
		Qc(1,15)	= xUx - 8.0
		Qc(1,16)	= xUx
!------------------------------ 
	else if(iGeo == 1) Then		!---------------------- C1 todo
!----------------------- C1
		Qc(1,2)		= 8.0D0/3.0D0
		Qc(1,3)		= 8.0
		Qc(1,4)		= xLx
!	-----------------------------
		aa			= xLx + (xUx - xLx)/3.
!	-----------------------------
		Qc(1,6)		= 56.0D0/9.0D0
		Qc(1,7)		= aa - 8.0
		Qc(1,8)		= aa
!	-----------------------------
		aa			= xLx + 2.*(xUx - xLx)/3.
!	-----------------------------
		Qc(1,10)	= 88.0D0/9.0D0
		Qc(1,11)	= aa - 8.0
		Qc(1,12)	= aa
		Qc(1,14)	= 40.0D0/3.0D0
		Qc(1,15)	= xUx - 8.0
		Qc(1,16)	= xUx
	end if
!----------------------- Zero Twist Override
!	Qc(1,6)		= 16.0D0/3.0D0
!	Qc(1,7)		= 16.0D0
!	Qc(1,10)	= 32.0D0/3.0D0
!	Qc(1,11)	= 16.0D0
!-----------------------------
!
	xLy4		= xLy/4.
	x2Ly4		= 2.*xLy4
	x3Ly4		= 3.*xLy4
!
!	Qc(2,1)		= 0.
!	Qc(2,2)		= 0.
!	Qc(2,3)		= 0.
!	Qc(2,4)		= 0
!	Qc(2,5)		= 0
!
	Qc(2,6)		= xLy4
	Qc(2,7)		= xLy4
	Qc(2,8)		= xLy4
	Qc(2,9)		= xLy4
	Qc(2,10)	= xLy4
!
	Qc(2,11)	= x2Ly4
	Qc(2,12)	= x2Ly4
	Qc(2,13)	= x2Ly4
	Qc(2,14)	= x2Ly4
	Qc(2,15)	= x2Ly4
!
	Qc(2,16)	= x3Ly4
	Qc(2,17)	= x3Ly4
	Qc(2,18)	= x3Ly4
	Qc(2,19)	= x3Ly4
	Qc(2,20)	= x3Ly4
!
	Qc(2,21)	= xLy
	Qc(2,22)	= xLy
	Qc(2,23)	= xLy
	Qc(2,24)	= xLy
	Qc(2,25)	= xLy
  100 continue
!
	return
	end
	
