	Subroutine FormHierarchy_3to4(T,nR,nC,iOut)
!
!-----------------------------------
	Implicit Real(kind=8) (a-h,o-z)
!
!	Input Variables & Arrays
!
!	
!	Output Variables & Arrays	
!
	Real(kind=8)	T   
	Dimension		T(nR,nC)
	
!	//////////////////////
!	Transformation Matrix 
!
	T		= 0.0D0	! everything zeroed
!------------------------
	T( 1, 1) =  1.0D0 
!
	T( 2, 1) = -1.0D0/6.0D0 
	T( 2, 2) =  2.0D0/3.0D0 
	T( 2, 3) =  1.0D0 
	T( 2, 4) = -2.0D0/3.0D0 
	T( 2, 5) =  1.0D0/6.0D0 
!
	T( 2, 1) =  1.0D0/6.0D0 
	T( 2, 2) = -2.0D0/3.0D0 
	T( 2, 3) =  1.0D0 
	T( 2, 4) =  2.0D0/3.0D0 
	T( 2, 5) = -1.0D0/6.0D0 
!
	T( 4, 5) =  1.0D0 
!------------------------
	T( 5, 6) =  1.0D0 
!
	T( 6, 6) = -1.0D0/6.0D0 
	T( 6, 7) =  2.0D0/3.0D0 
	T( 6, 8) =  1.0D0 
	T( 6, 9) = -2.0D0/3.0D0 
	T( 6,10) =  1.0D0/6.0D0 
!
	T( 7, 6) =  1.0D0/6.0D0 
	T( 7, 7) = -2.0D0/3.0D0 
	T( 7, 8) =  1.0D0 
	T( 7, 9) =  2.0D0/3.0D0 
	T( 7,10) = -1.0D0/6.0D0 
!
	T( 8,10) =  1.0D0 
!------------------------
	T( 9,11) =  1.0D0 
!
	T(10,11) = -1.0D0/6.0D0 
	T(10,12) =  2.0D0/3.0D0 
	T(10,13) =  1.0D0 
	T(10,14) = -2.0D0/3.0D0 
	T(10,15) =  1.0D0/6.0D0 
!
	T(11,11) =  1.0D0/6.0D0 
	T(11,12) = -2.0D0/3.0D0 
	T(11,13) =  1.0D0 
	T(11,14) =  2.0D0/3.0D0 
	T(11,15) = -1.0D0/6.0D0 
!
	T(12,15) =  1.0D0 
!------------------------
	write(iOut,1000) nR,nC
	do 110 i = 1,nR
		write(iOut,1010) i,(T(i,j),j = 1,nC)
  110	continue
  120 continue
!
!
 1000 format("4to3 Hierarchy:T(",I2,",",I2,")"/)
 1010 format(I2,2X,15(f5.2,1X))
   		 
!   		 
	return
	end   		